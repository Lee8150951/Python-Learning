tourist = ['Shanghai', 'Shaanxi', 'Tibet', 'Sichuan', 'Xinjiang']
print(tourist)
# 临时排序
print(sorted(tourist))
print(tourist)
# 反置
tourist.reverse()
print(tourist)
tourist.reverse()
print(tourist)
# 正排序
tourist.sort()
print(tourist)
# 反向字母排序
tourist.sort(reverse=True)
print(tourist)

